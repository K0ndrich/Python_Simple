# Операторы NOT , AND , OR

# оператор not переворачивает значение
# not True  -> False
# not False -> True
not 4 == 4  # ->False
not 4 != 4  # -> True

# оператор and возвращает True только если оба значения True
# в других случаях возвращает False
# True and True -> True
1 > 0 and 2 > 0  # -> True
