# LEN функция
# Функция len() возвращает количество елементом(длину) в обьекте

# длина равно нулю 0
len("")  # -> 0

# длира равно 1, бо пробел ето тоже елемент
len(" ")  # -> 1

# длина равно трем 3 елементам строки
len("abc")  # -> 3

# передаем внутрь функции переменную
a = "hello"
len(a)  # -> 5

"Word is have lenght", len(a)  # -> Word is have lenght 5